%利用迭代法求解差分方程
%差分方程为：y(n) - (3/4)y(n - 1) + (1/8)y(n - 2) = x(n) + (1/3)x(n - 1)
%输入信号x(n) = (3/4)^n
%初始条件y(-1) = 4,y(-2) = 12
a = [1 -3/4 1/8];%y侧的系数向量
b = [1 1/3 0];%x侧的系数向量
zx = [0 0];zy = [4 12];%x,y的初始条件
n = 0 : 10;%求n = 0 : 10时
xn = (3/4) .^ n;%n = 0 : 10的输入信号
zi = filtic(b,a,zy,zx);%计算等效初始条件
[yn,zf] = filter(b,a,xn,zi);%迭代计算输出和后段等效初始条件